function useIdleGlitch() {
  const [glitching, setGlitching] = useState(false);
  useEffect(() => {
    let timeout: ReturnType<typeof setTimeout>;
    const schedule = () => {
      timeout = setTimeout(() => {
        setGlitching(true);
        setTimeout(() => { setGlitching(false); schedule(); }, 140);
      }, 3500 + Math.random() * 5000);
    };
    schedule();
    return () => clearTimeout(timeout);
  }, []);
  return glitching;
}

function useNodeGlitch(active: boolean) {
  const [g, setG] = useState(false);
  useEffect(() => {
    if (!active) return;
    let t: ReturnType<typeof setTimeout>;
    const go = () => { setG(true); t = setTimeout(() => setG(false), 120); };
    go();
    const id = setInterval(go, 2800 + Math.random() * 2000);
    return () => { clearInterval(id); clearTimeout(t); };
  }, [active]);
  return g;
}


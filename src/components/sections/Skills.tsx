function TreeEdges({ edges, nodes, activeNode, layout }: EdgesProps) {
  return (
    <svg
      className="absolute inset-0 pointer-events-none overflow-visible"
      style={{ width: '100%', height: '100%', zIndex: 0 }}
    >
      <defs>
        <filter id="glow-soft">
          <feGaussianBlur stdDeviation="2" result="b"/>
          <feMerge><feMergeNode in="b"/><feMergeNode in="SourceGraphic"/></feMerge>
        </filter>
        <filter id="glow-strong">
          <feGaussianBlur stdDeviation="3.5" result="b"/>
          <feMerge><feMergeNode in="b"/><feMergeNode in="SourceGraphic"/></feMerge>
        </filter>
        <marker id="dot-normal" markerWidth="4" markerHeight="4" refX="2" refY="2">
          <circle cx="2" cy="2" r="1.6" fill="rgba(255,0,60,0.4)"/>
        </marker>
        <marker id="dot-active" markerWidth="6" markerHeight="6" refX="3" refY="3">
          <circle cx="3" cy="3" r="2.5" fill="#ff003c" filter="url(#glow-soft)"/>
        </marker>
      </defs>
      {edges.map((edge, idx) => {
        const from = nodes.find(n => n.id === edge.from);
        const to   = nodes.find(n => n.id === edge.to);
        if (!from || !to) return null;

        const isActive = activeNode === edge.from || activeNode === edge.to;

        const x1 = (from.x / 100) * layout.width;
        const y1 = from.y * layout.rowHeight + layout.nodeHeight;
        const x2 = (to.x  / 100) * layout.width;
        const y2 = to.y   * layout.rowHeight;

        // Bus routing: drop vertically, then horizontal, then up
        const busY = y1 + 18;
        const sameRow = Math.abs(from.y - to.y) <= 1;
        const d = sameRow && Math.abs(x1 - x2) > 4
          ? `M ${x1} ${y1} L ${x1} ${busY} L ${x2} ${busY} L ${x2} ${y2}`
          : `M ${x1} ${y1} L ${x2} ${y2}`;

        const junctions = sameRow && Math.abs(x1 - x2) > 4
          ? [{ cx: x1, cy: busY }, { cx: x2, cy: busY }]
          : [];

        return (
          <g key={`${edge.from}-${edge.to}-${idx}`}>
            {/* Base line */}
            <path
              d={d}
              fill="none"
              stroke={isActive ? '#ff003c' : 'rgba(255,0,60,0.22)'}
              strokeWidth={isActive ? 1.6 : 0.9}
              filter={isActive ? 'url(#glow-strong)' : undefined}
              style={{ transition: 'stroke 0.2s, stroke-width 0.2s' }}
            />
            {/* Animated signal pulse */}
            {isActive && (
              <path
                d={d}
                fill="none"
                stroke="rgba(255,180,180,0.6)"
                strokeWidth={0.9}
                strokeDasharray="4 9"
                style={{
                  animation: 'skillPulse 1.1s linear infinite',
                }}
              />
            )}
            {/* Junction dots */}
            {junctions.map((j, ji) => (
              <circle
                key={ji}
                cx={j.cx}
                cy={j.cy}
                r={isActive ? 2.8 : 1.8}
                fill={isActive ? '#ff003c' : 'rgba(255,0,60,0.4)'}
                filter={isActive ? 'url(#glow-soft)' : undefined}
                style={{ transition: 'r 0.2s, fill 0.2s' }}
              />
            ))}
          </g>
        );
      })}
    </svg>
  );
}

